function applyTheme(theme) {
  const isLight = theme === "light";
  document.body.classList.toggle("light", isLight);
  if (themeIcon) themeIcon.textContent = isLight ? "☀️" : "🌙";
  localStorage.setItem("theme", theme);
}

function initializeTheme() {
  const savedTheme = localStorage.getItem("theme");
  if (savedTheme === "light" || savedTheme === "dark") {
    applyTheme(savedTheme);
    return;
  }
  const prefersLight = window.matchMedia("(prefers-color-scheme: light)").matches;
  applyTheme(prefersLight ? "light" : "dark");
}

function setLanguage(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.documentElement.lang = lang;
  localStorage.setItem("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });

  langButtons.forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
}

function initializeLanguage() {
  const saved = localStorage.getItem("lang");
  setLanguage(saved && translations[saved] ? saved : "uz");
}

function closeMobileMenu() {
  navLinks.classList.remove("open");
}

function loadCertificates() {
  const grid = document.querySelector("#certsGrid");
  if (!grid) return;

  const found = [];

  CERT_IMAGES.forEach((src) => {
    const img = new Image();
    img.onload = () => {
      if (!found.includes(src)) {
        found.push(src);
        renderCerts(found);
      }
    };
    img.src = src;
  });
}

function renderCerts(sources) {
  const grid = document.querySelector("#certsGrid");
  if (!sources.length) return;

  grid.innerHTML = sources
    .map(
      (src) =>
        `<div class="cert-item"><img src="${src}" alt="Certificate" loading="lazy" /></div>`
    )
    .join("");
}

